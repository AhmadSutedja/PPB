package com.example.clickcounter;

public class ListJari {
    String jari;
    int gambar;

    public ListJari(String jari, int gambar) {
        this.jari = jari;
        this.gambar = gambar;
    }

    public String getJari() {
        return jari;
    }

    public int getGambar() {
        return gambar;
    }
}
